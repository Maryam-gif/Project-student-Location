<h1>About the Project</h1>
<p>The main objective of this project is to develop a system that can help locate students within a building. The system will use MQTT as a sensor for detecting the presence of students in specific areas of the building. The sensor data will be stored in a database, which will be managed by Superbase, a cloud-based database management system. To develop and manage the system, the Visual Studio Code (VS Code) development environment will be used for the front end.<br>
    <br>The system will consist of several sensors that will be placed in different areas of the building. When a student enters a particular area, the sensor detects their presence and sends a signal to the database. This information will be processed and made available to authorized users, who can view the location.<br>
    <br>The system will be designed to ensure the safety and security of the students. It will also help increase efficiency in locating students within the building, especially in emergencies. The system will be tested and evaluated to ensure that it meets the requirements and objectives set out in the project plan.</p>
